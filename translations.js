// Language management
const LanguageManager = {
    currentLang: localStorage.getItem('csmr_language') || 'en',
    
    init() {
        this.setLanguage(this.currentLang);
        this.setupLanguageSelector();
    },
    
    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('csmr_language', lang);
        document.documentElement.lang = lang;
        
        // Update all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                // Check if element contains HTML (like buttons with icons)
                if (element.querySelector('i, span, div')) {
                    // Preserve HTML structure, only update text nodes
                    const textNodes = Array.from(element.childNodes).filter(node => node.nodeType === 3);
                    if (textNodes.length > 0) {
                        textNodes[0].textContent = translations[lang][key];
                    } else {
                        // If no text node, replace innerHTML but preserve structure
                        const html = element.innerHTML;
                        const iconMatch = html.match(/<i[^>]*>.*?<\/i>/);
                        if (iconMatch) {
                            element.innerHTML = iconMatch[0] + ' ' + translations[lang][key];
                        } else {
                            element.textContent = translations[lang][key];
                        }
                    }
                } else {
                    element.textContent = translations[lang][key];
                }
            }
        });
        
        // Update placeholders
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            if (translations[lang] && translations[lang][key]) {
                element.placeholder = translations[lang][key];
            }
        });
        
        // Update language selector
        const selectors = document.querySelectorAll('.language-selector');
        selectors.forEach(selector => {
            selector.value = lang;
        });
    },
    
    setupLanguageSelector() {
        document.querySelectorAll('.language-selector').forEach(selector => {
            selector.value = this.currentLang;
            selector.addEventListener('change', (e) => {
                this.setLanguage(e.target.value);
                // Reload page to apply all translations
                window.location.reload();
            });
        });
    },
    
    getTranslation(key) {
        return translations[this.currentLang]?.[key] || key;
    }
};

// Initialize on page load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => LanguageManager.init());
} else {
    LanguageManager.init();
}

